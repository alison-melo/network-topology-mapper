/**
 * Looks up a MAC address to determine its vendor and likely device type.
 * @param mac MAC address in format XX:XX:XX:XX:XX:XX
 */
export function getDeviceInfoFromMac(mac: string): { vendor: string; type: Device['type'] } | null {
  if (!mac) return null;
  
  const prefix = mac.toUpperCase().substring(0, 8); // e.g., "00:1C:B3"
  
  if (MAC_OUI_DB[prefix]) {
    return MAC_OUI_DB[prefix];
  }
  
  return null;
}
